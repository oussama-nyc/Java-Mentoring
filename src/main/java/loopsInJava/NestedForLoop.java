public class NestedForLoop {

    public static void main(String[] args) {

        // Nested For Loop
        
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 2; j++) {
                System.out.println(i);
            }
            System.out.println();
        }
    }

}
